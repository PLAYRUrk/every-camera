# ---------------------------------------------------------------------------
# Core computation
# ---------------------------------------------------------------------------
def compute_sun_on_off(date, location, angle):
    """
    Find the times when the sun crosses a given altitude angle.

    Parameters:
        date: datetime (start of day, UTC)
        location: astropy EarthLocation
        angle: float, altitude angle in degrees

    Returns:
        (sunrise_time, sunset_time) as datetime objects or (None, None)
        if the sun never reaches the angle on this day.
    """
    from astropy.coordinates import get_sun, AltAz
    from astropy.time import Time

    prev_alt = None
    sunrise_time = None
    sunset_time = None

    for h in range(24):
        for m in range(60):
            t = date + timedelta(hours=h, minutes=m)
            astro_time = Time(t, scale='utc')
            altaz = AltAz(obstime=astro_time, location=location)
            sun_alt = get_sun(astro_time).transform_to(altaz).alt.degree

            if prev_alt is not None:
                rising = sun_alt > prev_alt
                if rising and sunrise_time is None and sun_alt > angle and sun_alt < angle + 1:
                    sunrise_time = t
                if not rising and sunset_time is None and sun_alt < angle:
                    sunset_time = t

            prev_alt = sun_alt

    return sunrise_time, sunset_time


def generate_schedule(lat, lon, alt_m, angle, start_date, num_days,
                      progress_cb=None):
    """
    Generate schedule entries for a range of dates.

    Returns list of (date_str, sunrise_utc, sunset_utc) tuples.
    Entries where the sun never reaches the angle are skipped.
    """
    from astropy.coordinates import EarthLocation
    from astropy import units as u

    location = EarthLocation(lon=lon * u.degree, lat=lat * u.degree,
                             height=alt_m * u.meter)

    results = []
    for day_idx in range(num_days):
        current_date = start_date + timedelta(days=day_idx)
        sunrise, sunset = compute_sun_on_off(current_date, location, angle)

        if sunrise and sunset:
            results.append({
                "date": current_date.strftime("%d.%m.%Y"),
                "start": sunrise,
                "end": sunset,
            })

        if progress_cb:
            progress_cb(day_idx + 1, num_days)

    return results


def write_schedule_file(filepath, entries):
    """Write schedule entries to a file in Every Camera format."""
    from utils import SCHEDULE_DT_FMT
    with open(filepath, "w") as f:
        f.write("# Measurement Schedule (generated by schedule_generator.py)\n")
        f.write(f"# Format: {SCHEDULE_DT_FMT} - {SCHEDULE_DT_FMT}\n")
        f.write(f"# Generated: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}\n\n")
        for e in entries:
            start_str = e["start"].strftime(SCHEDULE_DT_FMT)
            end_str = e["end"].strftime(SCHEDULE_DT_FMT)
            f.write(f"{start_str} - {end_str}\n")


