#!/usr/bin/env python3
"""
Solar schedule generator for Every Camera.

Calculates sunrise/sunset times for a given solar altitude angle
and generates a schedule file compatible with Canon and Infra cameras.

Usage:
    # Console mode (interactive)
    python schedule_generator.py

    # Console mode with arguments
    python schedule_generator.py --lat 51.8106 --lon 103.0755 --alt 680 \
        --angle -10 --start 2026-01-01 --days 100 --output schedule.txt

    # GUI mode
    python schedule_generator.py --gui
"""
