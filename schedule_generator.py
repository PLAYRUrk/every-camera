def run_console(args):
    """Interactive or argument-based console schedule generation."""
    lat = args.lat
    lon = args.lon
    alt_m = args.alt
    angle = args.angle
    start = args.start
    days = args.days
    output = args.output

    # Interactive mode if required params missing
    if lat is None or lon is None:
        print("=" * 60)
        print("  Solar Schedule Generator")
        print("=" * 60)
        print("\nPredefined sites:")
        for i, (key, site) in enumerate(SITES.items(), 1):
            print(f"  {i}. {site['name']} ({key}): "
                  f"lat={site['lat']:.4f}, lon={site['lon']:.4f}, alt={site['alt']}m")
        print(f"  0. Custom coordinates")

        try:
            choice = int(input("\nSelect site [0]: ").strip() or "0")
        except ValueError:
            choice = 0

        if 1 <= choice <= len(SITES):
            site_key = list(SITES.keys())[choice - 1]
            site = SITES[site_key]
            lat = site["lat"]
            lon = site["lon"]
            alt_m = site["alt"]
            print(f"\nSelected: {site['name']}")
        else:
            lat = float(input("Latitude (degrees): ").strip())
            lon = float(input("Longitude (degrees): ").strip())
            alt_m = float(input("Altitude (meters) [0]: ").strip() or "0")

    if angle is None:
        angle = float(input("Sun altitude angle (degrees, e.g. -10): ").strip() or "-10")

    if start is None:
        start_str = input(f"Start date (YYYY-MM-DD) [{datetime.now().strftime('%Y-%m-%d')}]: ").strip()
        start = datetime.strptime(start_str, "%Y-%m-%d") if start_str else datetime.now().replace(
            hour=0, minute=0, second=0, microsecond=0)

    if days is None:
        days = int(input("Number of days [100]: ").strip() or "100")

    if output is None:
        output = input("Output file [schedule.txt]: ").strip() or "schedule.txt"

    print(f"\nParameters:")
    print(f"  Location  : lat={lat:.4f}, lon={lon:.4f}, alt={alt_m}m")
    print(f"  Angle     : {angle} degrees")
    print(f"  Period    : {start.strftime('%Y-%m-%d')} + {days} days")
    print(f"  Output    : {output}")
    print()

    def progress(current, total):
        pct = current * 100 // total
        bar = "#" * (pct // 2) + "-" * (50 - pct // 2)
        print(f"\r  [{bar}] {current}/{total} days", end="", flush=True)

    print("Computing solar angles...")
    results = generate_schedule(lat, lon, alt_m, angle, start, days,
                                progress_cb=progress)
    print()

    if not results:
        print("No schedule entries generated (sun never reaches the specified angle).")
        return

    write_schedule_file(output, results)
    print(f"\nGenerated {len(results)} schedule entries -> {output}")
    print("\nPreview (first 10 entries):")
    for e in results[:10]:
        print(f"  {e['date']}  {e['start'].strftime('%H:%M')} - {e['end'].strftime('%H:%M')} UTC")
    if len(results) > 10:
        print(f"  ... and {len(results) - 10} more")


